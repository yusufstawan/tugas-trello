// Loop with Range
// Buatlah script untuk mencetak dengan rentang angka tertentu.

// Contoh
// Input pertama: 4
// Input kedua: 8
// Output di browser: 4, 5, 6, 7, 8

function loopWithRange(start, end) {
  for (var i = start; i <= end; i++) {
    console.log(i);
  }
}

console.log(loopWithRange(4, 8));